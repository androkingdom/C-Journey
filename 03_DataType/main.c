#include <stdio.h>

int main() {
    int a = 10;               // Integer
    float b = 5.5;            // Floating-point
    char c = 'A';             // Character
    double d = 10.5;          // Double precision floating-point
    unsigned int e = 20;      // Unsigned integer

    printf("int: %d\n", a);
    printf("float: %f\n", b);
    printf("char: %c\n", c);
    printf("double: %lf\n", d);
    printf("unsigned int: %u\n", e);

    return 0;
}
